/* Svas hero rotation engine — powers the homepage hero, the waitlist showcase
   and the "India's food" marquee. Vanilla JS, no deps, same motion language as
   the app: calm crossfades (700ms hero band), 400ms eased count-ups, nothing
   bouncy. Reads window.SVAS_ROTATION (generated by scripts/build_rotation.js).

   Markup contract (any page):
     <div data-rotation data-interval="5000">   ← rotation root
       <img data-rot-img-a> <img data-rot-img-b>  ← stacked crossfade layers
       [data-rot-state] [data-rot-native] [data-rot-name]
       [data-rot-kcal] [data-rot-p] [data-rot-f] [data-rot-c]  ← count-up targets
     </div>
     <div data-gallery></div>                   ← marquee strip (optional)
*/
(function () {
  var DATA = window.SVAS_ROTATION;
  if (!DATA) return;
  var reduced = window.matchMedia && window.matchMedia("(prefers-reduced-motion: reduce)").matches;

  // Supabase storage → edge-cached right-sized render (width-only + contain:
  // pure proportional downscale, never a crop — same rule as the app).
  function img(uri, w) {
    var r = uri.replace("/storage/v1/object/public/", "/storage/v1/render/image/public/");
    return r === uri ? uri : r + "?width=" + w + "&quality=70&resize=contain";
  }

  /* ---- eased count-up (easeOutCubic — numbers settle, never flash) */
  function tweenNum(el, to, dur) {
    var from = parseInt(el.dataset.val || "0", 10) || 0;
    el.dataset.val = String(to);
    if (reduced || from === to) { el.textContent = to; return; }
    var t0 = null, DUR = dur || 400;
    function step(ts) {
      if (!t0) t0 = ts;
      var t = Math.min((ts - t0) / DUR, 1);
      var e = 1 - Math.pow(1 - t, 3);
      el.textContent = Math.round(from + (to - from) * e);
      if (t < 1) requestAnimationFrame(step);
    }
    requestAnimationFrame(step);
  }

  /* ---------------------------------------------------------- rotation root */
  function initRotation(root) {
    var dishes = DATA.hero;
    var interval = parseInt(root.dataset.interval || "5000", 10);
    var w = parseInt(root.dataset.imgw || "720", 10);
    var layers = [root.querySelector("[data-rot-img-a]"), root.querySelector("[data-rot-img-b]")];
    var q = function (sel) { return root.querySelector(sel); };
    var els = {
      state: q("[data-rot-state]"), native: q("[data-rot-native]"), name: q("[data-rot-name]"),
      kcal: q("[data-rot-kcal]"), p: q("[data-rot-p]"), f: q("[data-rot-f]"), c: q("[data-rot-c]"),
    };
    var i = 0, front = 0, timer = null, gen = 0;
    var SYNC = 700; // one shared duration — matches --m-hero, so photo,
                    // text fades and count-ups start AND finish together

    function setText(el, v, instant) {
      if (!el) return;
      if (instant || reduced) { el.textContent = v; return; }
      el.classList.add("rot-out"); // fade out SYNC/2 …
      setTimeout(function () { el.textContent = v; el.classList.remove("rot-out"); }, SYNC / 2); // … fade back SYNC/2
    }

    function show(idx, instant) {
      var d = dishes[idx];
      var my = ++gen; // a newer show() call cancels this one's pending apply
      var back = 1 - front;
      var l = layers[back];

      // everything fires in the SAME frame, everything runs SYNC ms
      function apply() {
        if (my !== gen) return;
        if (l) {
          l.classList.add("on");
          if (layers[front]) layers[front].classList.remove("on");
          front = back;
        }
        var inst = instant || reduced;
        setText(els.state, d.state, inst);
        setText(els.native, d.native, inst);
        setText(els.name, d.name, inst);
        if (els.kcal) tweenNum(els.kcal, d.kcal, SYNC);
        if (els.p) tweenNum(els.p, d.p, SYNC);
        if (els.f) tweenNum(els.f, d.f, SYNC);
        if (els.c) tweenNum(els.c, d.c, SYNC);
      }

      if (!l) return apply();
      l.src = img(d.img, w);
      l.alt = d.name + " — " + d.state;
      if (instant || reduced) return apply();
      // hold the text until the photo is decoded, then start both together
      // (images are preloaded after first paint, so this is normally instant)
      var ready = l.decode ? l.decode().catch(function () {}) : Promise.resolve();
      ready.then(function () { requestAnimationFrame(apply); });
    }

    show(0, true);
    if (reduced || dishes.length < 2) return;

    function tick() { i = (i + 1) % dishes.length; show(i); }
    function start() { if (!timer) timer = setInterval(tick, interval); }
    function stop() { clearInterval(timer); timer = null; }
    document.addEventListener("visibilitychange", function () {
      document.hidden ? stop() : start();
    });
    start();

    // preload the rest after first paint so crossfades never pop in blank
    window.addEventListener("load", function () {
      dishes.forEach(function (d) { new Image().src = img(d.img, w); });
    });
  }

  /* ------------------------------- native-names ticker (3 floating rows) */
  function initTicker(root) {
    var ROWS = 3, SPEEDS = [72, 88, 64]; // seconds per loop — each row drifts differently
    function item(d) {
      var s = document.createElement("span");
      s.className = "tk-item";
      s.tabIndex = 0;
      s.innerHTML =
        '<span class="tk-chip">' + d.p + "g protein · " + d.kcal + " kcal</span>" +
        '<b class="tk-native">' + d.native + "</b>" +
        '<span class="tk-name">' + d.name + " · " + d.state + "</span>";
      s.addEventListener("click", function () { s.classList.toggle("open"); }); // mobile: tap reveals
      return s;
    }
    for (var r = 0; r < ROWS; r++) {
      var row = document.createElement("div");
      row.className = "tk-row" + (r === 1 ? " rev" : ""); // middle row drifts the other way
      row.style.animationDuration = SPEEDS[r] + "s";
      var dishes = DATA.ticker.filter(function (_, i) { return i % ROWS === r; });
      dishes.forEach(function (d) { row.appendChild(item(d)); });
      if (!reduced) dishes.forEach(function (d) { row.appendChild(item(d)); }); // duplicate → seamless loop
      root.appendChild(row);
    }
    if (reduced) root.classList.add("static");
  }

  /* --------------------------------- entrance — land after the intro lifts */
  function land() { document.body.classList.add("landed"); }
  function initLanding() {
    var intro = document.getElementById("svas-intro");
    if (!intro || reduced) return land();
    var done = false;
    var fin = function () { if (!done) { done = true; land(); } };
    new MutationObserver(function () {
      if (!document.getElementById("svas-intro") || intro.classList.contains("out")) fin();
    }).observe(document.body, { childList: true, subtree: false, attributes: true, attributeFilter: ["class"] });
    new MutationObserver(function () {
      if (intro.classList.contains("out")) fin();
    }).observe(intro, { attributes: true, attributeFilter: ["class"] });
    setTimeout(fin, 9000); // hard ceiling — never leave the page unrevealed
  }

  document.querySelectorAll("[data-rotation]").forEach(initRotation);
  document.querySelectorAll("[data-ticker]").forEach(initTicker);
  initLanding();
})();
